import UIKit

public extension UIColor {
    
    public static func cyan50() -> UIColor {
        return UIColor(red: 224/255, green: 247/255, blue: 250/255, alpha: 1)
    }
    
    public static func cyan100() -> UIColor {
        return UIColor(red: 178/255, green: 235/255, blue: 242/255, alpha: 1)
    }
    
    public static func cyan150() -> UIColor {
        return UIColor(red: 153/255, green: 228/255, blue: 238/255, alpha: 1)
    }
    
    public static func cyan200() -> UIColor {
        return UIColor(red: 128/255, green: 222/255, blue: 234/255, alpha: 1)
    }
    
    public static func cyan300() -> UIColor {
        return UIColor(red: 77/255, green: 208/255, blue: 225/255, alpha: 1)
    }
    
    public static func teal200() -> UIColor {
        return UIColor(red: 128/255, green: 203/255, blue: 196/255, alpha: 1)
    }
}
